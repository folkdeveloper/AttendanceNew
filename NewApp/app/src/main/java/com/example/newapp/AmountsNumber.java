package com.example.newapp;

public class AmountsNumber {
    int amount;
    int number;

    public AmountsNumber() {

    }

    public AmountsNumber(int amount, int number) {
        this.amount = amount;
        this.number = number;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
